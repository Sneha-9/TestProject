package com.sneha.rangeGameTwo;

public class PointRange {
    private final int start;
    private final int end;

    PointRange(int start, int end) {
        this.start = start;
        this.end = end;
    }

    public int getEnd() {
        return end;
    }

    public int getStart() {
        return start;
    }
}
